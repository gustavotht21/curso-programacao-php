<?php

const NUMBER = 10;

if (NUMBER === 8) {
    print 'Igual a 8';
} else if (NUMBER === 9) {
    print 'Igual a 9';
} else if (NUMBER === 10) {
    print 'Igual a 10';
} else {
    print 'Número inválido';
}
print '<hr>';

// Ternário
const MEDIA = 7;

print (MEDIA >= 7) ? 'Aprovado' : 'Reprovado';
