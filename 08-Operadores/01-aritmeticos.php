<?php

// Aritméticos
print 'Soma -> + <br>';
print 6+2;
print '<br>';

print 'Diferença -> - <br>';
print 6-2;
print '<br>';

print 'Produto -> * <br>';
print 6*2;
print '<br>';

print 'Quociente -> / <br>';
print 6/2;
print '<br>';

print 'Módulo -> % <br>';
print 6%2;
print '<br>';

print 'Exponenciação -> ** <br>';
print 6**2;
print '<br>';

// Incremento e decremento

// Pré: Faz a ação depois retorna o valor
$number = 10;
print ++$number;
print '<br>';
// Pós: Retorna o valor depois faz a ação
print $number--;
print '<br>';
print $number;
